/** 検索語 → マッチすべき全バリエーションを返す */
export function expandSynonyms(input: string): string[] {
  const results = new Set<string>([input]);
  for (const group of synonymGroups) {
    if (group.some((term) => term.includes(input) || input.includes(term))) {
      for (const term of group) {
        results.add(term);
      }
    }
  }
  return [...results];
}
